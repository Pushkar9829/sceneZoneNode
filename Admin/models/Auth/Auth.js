const mongoose = require('mongoose')

const AdminAuth = new mongoose.Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      default: 'admin',
    },
  },
  { timestamps: true },
)

module.exports = mongoose.model('AdminAuth', AdminAuth)
